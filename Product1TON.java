
public class Product1TON {

	public static void main(String[] args) {
		long product=1;
		
		for(int i=1;i<=10;i++)
		{
			product=product*i;
		}
		System.out.println("Product form 1 to 10 is " +product);
		
		for(int i=11;i<=14;i++)
		{
			product=product*i;
			System.out.println("Product form 1 to " +i+ " is " +product);
		}
		

}



	}


